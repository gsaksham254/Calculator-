public class Calculate {
    public  int ADDITION(int num1, int num2){
        return num1 + num2;
    }
    public  int SUBTRACTION(int num1, int num2){
        return num1 - num2;
    }
    public int MULTIPLICATION(int num1, int num2){
        return num1 * num2;
    }
    public  int DIVISION(int num1, int num2){
        return num1 / num2;
    }
}
